public void clearFields(){
    idtxt.setText("");
    
    firstnametxt.setText("");
    middlenametxt.setText("");
    lastnametxt.setText("");
    if(malebttn.isSelected()==true){ 
        malebttn.setSelected(false);
    }
    if(femalebttn.isSelected()==true){
       femalebttn.setSelected(false);  
    }
   
    peraddresstxt.setText("");
    tempaddresstxt.setText("");
    mobiletxt.setText("");
    emailtxt.setText("");
    parent_nametxt.setText("");
    guardian_nametxt.setText("");
    parent_contacttxt.setText("");
    guardian_contacttxt.setText("");
    programtxt.setText("");
    semestertxt.setText("");
    sectiontxt.setText("");
    
}
