    private void editBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBttnActionPerformed
     if (editBttn.getText().equalsIgnoreCase("Edit")) {
            editBttn.setText("Update");
            StudentDTO userEdit = new StudentDAO().editStudent(studentTable);
            idtxt.setText(String.valueOf(userEdit.getId()));
            idtxt.setEditable(false);
            firstnametxt.setText(userEdit.getFirstname());
            middlenametxt.setText(userEdit.getMiddlename());
            lastnametxt.setText(userEdit.getLastname());
            peraddresstxt.setText(userEdit.getPeraddress());
            tempaddresstxt.setText(userEdit.getTempaddress());
            mobiletxt.setText(userEdit.getMobile());
            parent_nametxt.setText(userEdit.getParent_name());
            parent_contacttxt.setText(userEdit.getParent_contact());
            guardian_nametxt.setText(userEdit.getGuardian_name());
            guardian_contacttxt.setText(userEdit.getGuardian_contact());
            
            if(userEdit.getGender().equals("male")){
            malebttn.setSelected(true);
            }
            if(femalebttn.isSelected()==true){
            femalebttn.setSelected(true);
            }
            
            emailtxt.setText(userEdit.getEmail());
            sectiontxt.setText(userEdit.getSection());
            semestertxt.setText(userEdit.getSemester());
            
            
            loadDatas();

        } else {

            editBttn.setText("Edit");
            StudentDTO studentUpdate = new StudentDTO();
            studentUpdate.setId(Integer.parseInt(idtxt.getText()));
            
           
            studentUpdate.setFirstname(firstnametxt.getText());
            studentUpdate.setMiddlename(middlenametxt.getText());
            studentUpdate.setLastname(lastnametxt.getText());
            studentUpdate.setGuardian_name(guardian_nametxt.getText());
            studentUpdate.setGuardian_contact(guardian_contacttxt.getText());
            studentUpdate.setParent_name(parent_nametxt.getText());
            studentUpdate.setParent_contact(parent_contacttxt.getText());
            studentUpdate.setSection(sectiontxt.getText());
            studentUpdate.setSemester(semestertxt.getText());
            studentUpdate.setPeraddress(peraddresstxt.getText());
            studentUpdate.setTempaddress(tempaddresstxt.getText());
            studentUpdate.setMobile(mobiletxt.getText());
            studentUpdate.setEmail(emailtxt.getText());
            if(malebttn.isSelected()){
            studentUpdate.setGender("male");
            }
             if(femalebttn.isSelected()){
            studentUpdate.setGender("female");
            }
            
            studentUpdate.setProgram(programtxt.getText());
            new StudentDAO().updateStudent(studentUpdate);
            loadDatas();
            clearFields();
            idtxt.setEditable(true);

        }
    }//GEN-LAST:event_editBttnActionPerformed

